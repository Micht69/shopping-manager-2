package com.micht.shoppingmanager.bean;

/**
 * @author Micht
 *
 */
public abstract class CommonBean {
	/** ID of the object */
	public int id;
	/** Name of the object */
	public String name;

	public abstract String getName();

	public CommonBean(String pName) {
		this.name = pName;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + id;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		CommonBean other = (CommonBean) obj;
		if (id != other.id) {
			return false;
		}
		if (name == null) {
			if (other.name != null) {
				return false;
			}
		} else if (!name.equals(other.name)) {
			return false;
		}
		return true;
	}

}
